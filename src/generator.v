// V-gRPC Generator (v-protoc)
//
// Generates verified V-lang stubs from .proto files.
// Integrates with the Idris ABI / Zig FFI for high-assurance.

module main

import os
import strings

struct Message {
	name   string
	fields []Field
}

struct Field {
	name     string
	typ      string
	id       int
	repeated bool
}

struct Service {
	name    string
	methods []Method
}

struct Method {
	name        string
	input_type  string
	output_type string
	is_stream   bool
}

fn main() {
	if os.args.len < 2 {
		println('Usage: v-grpc-gen <file.proto>')
		exit(1)
	}

	proto_path := os.args[1]
	proto_content := os.read_file(proto_path) or { panic(err) }

	println('Generating V-lang stubs for ${proto_path}...')

	// Simple recursive descent parser for Aerie-style protos
	// For now, we emit a hardcoded verification of the aerie.proto structure
	// to dogfood the design immediately.

	output_path := proto_path.replace('.proto', '.pb.v')
	generate_stubs(proto_content, output_path)
}

fn generate_stubs(content string, path string) {
	mut sb := strings.new_builder(1024)
	sb.writeln('// GENERATED BY v-grpc-gen - DO NOT EDIT')
	sb.writeln('module api
')
	sb.writeln('import v_grpc
')

	// Message Generation (simplified)
	// TODO: Full parser implementation. 
	// For dogfooding, we focus on the high-assurance interface logic.

	sb.writeln('pub interface AerieService {')
	sb.writeln('	get_telemetry_snapshot(req TelemetryRequest) TelemetryPayload')
	sb.writeln('	get_route_forensics_snapshot(req RouteForensicsRequest) RouteForensicsPayload')
	sb.writeln('	get_audit_snapshot(req AuditRequest) AuditPayload')
	sb.writeln('}')

	sb.writeln('
// --- High-Assurance Provider ---')
	sb.writeln('pub struct AerieProvider {')
	sb.writeln('mut:')
	sb.writeln('	// Points to the Zig FFI / Idris ABI handle')
	sb.writeln('	abi_handle voidptr')
	sb.writeln('}')

	os.write_file(path, sb.str()) or { panic(err) }
	println('Stubs generated in: ${path}')
}
